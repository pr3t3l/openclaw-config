def build_user_prompt(doc, content, case_info, images, experience_notes=''):
    """Build the user prompt for a specific document."""
    doc_id = doc.get('doc_id', '?')
    type_key = doc.get('type_key', 'unknown')
    envelope = doc.get('envelope', 'A')
    title = doc.get('in_world_title', '')
    timestamp = doc.get('timestamp', '')
    date = timestamp[:10] if timestamp else ''

    # Format images for the prompt
    image_section = ''
    if images:
        image_lines = []
        for i, img in enumerate(images, 1):
            purpose = img.get('purpose', 'document image')
            poi = img.get('poi', '')
            desc = f"Image {i}"
            if poi:
                desc += f" (POI: {poi})"
            desc += f": {purpose}"
            image_lines.append(f'{desc}')
            image_lines.append(f'<img src="{img["data_uri"]}" />')
        image_section = '\n'.join(image_lines)
    else:
        image_section = '(No images for this document — use text and layout only)'

    # Experience/emotional notes
    exp_section = ''
    if experience_notes:
        exp_section = f'\nEMOTIONAL BEAT / DETECTIVE NOTES:\n{experience_notes}\n'

    return f"""Design document {doc_id} (type: {type_key}) for this case.

CASE: {case_info.get('case_number', 'CASE-001')}
DOCUMENT ID: {doc_id}
ENVELOPE: {envelope}
TITLE: {title}
DATE: {date}

CONTENT (this is the narrative content — incorporate ALL of it into the document design):
---
{content}
---

IMAGES (embed these using the base64 data URIs provided):
{image_section}
{exp_section}
Generate the complete HTML document now. Start with <!DOCTYPE html>."""


