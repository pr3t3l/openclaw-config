def resolve_images_for_doc(doc_id, art_briefs, case_dir):
    """Find ONLY POI portrait images for a document. All other visuals are created by Claude in HTML."""
    images = []
    for brief in art_briefs.get('briefs', []):
        if not isinstance(brief, dict):
            continue
        fn = brief.get('filename', '')
        is_poi = 'poi_' in fn.lower() or 'portrait' in fn.lower() or 'mugshot' in fn.lower()
        if not is_poi:
            continue

        # Check if this brief is for this document
        matches = False
        # Method 1: direct for_doc field
        if brief.get('for_doc') == doc_id:
            matches = True
        # Method 2: usage_map array (backwards compatibility)
        for usage in brief.get('usage_map', []):
            if usage.get('doc_id') == doc_id:
                matches = True
                break

        if not matches:
            continue

        poi = brief.get('for_poi', '')
        # Try paths: fn might already have visuals/canonical/ prefix
        search_paths = [
            case_dir / fn,
            case_dir / 'visuals' / 'canonical' / Path(fn).name,
        ]
        for sp in search_paths:
            if sp.exists():
                data_uri = resize_image_to_base64(sp, POI_IMAGE_MAX_WIDTH)
                if data_uri:
                    images.append({
                        'slot': brief.get('image_id', ''),
                        'purpose': f'POI portrait: {poi}',
                        'poi': poi,
                        'data_uri': data_uri,
                        'filename': fn,
                        'original_path': str(sp)
                    })
                break
    return images


