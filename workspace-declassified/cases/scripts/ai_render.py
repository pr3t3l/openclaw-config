def render_document(doc, case_dir, workspace, design_system, type_catalog,
                    art_briefs, case_info, experience, model, tracker=None):
    """Render a single document using Claude API."""
    doc_id = doc.get('doc_id', '?')
    type_key = doc.get('type_key', '')
    envelope = doc.get('envelope', 'A')

    # Read content
    content_path = case_dir / f'envelope_{envelope}' / f'{doc_id}_content.md'
    content = read_file(content_path)
    if not content:
        print(f'  SKIP {doc_id}: no content file')
        return False

    # Get type design info
    if type_key in type_catalog.get('types', {}):
        doc_type_info = type_catalog['types'][type_key]
    else:
        # Custom type — use design_hint from clue_catalog
        doc_type_info = {
            'feel': doc.get('design_hint', f'A {type_key.replace("_", " ")} document.'),
            'layout': 'Professional document layout appropriate for this type.',
            'typography': 'Appropriate fonts for this document type.',
            'special_elements': [],
            'page_estimate': '1-2 pages',
            'background': 'White',
            'is_custom': True
        }

    # Resolve images
    images = resolve_images_for_doc(doc_id, art_briefs, case_dir)

    # Get experience notes from document_experience_map
    exp_notes = ''
    if experience:
        doc_exp = experience.get('document_experience_map', {}).get(doc_id, {})
        if doc_exp:
            parts = []
            beat = doc_exp.get('emotional_beat', '')
            if beat:
                parts.append(f'EMOTIONAL BEAT: {beat}')
            annotations = doc_exp.get('detective_annotations', [])
            if annotations:
                parts.append('DETECTIVE ANNOTATIONS:')
                for ann in annotations:
                    parts.append(f'  [{ann.get("type")}] {ann.get("content")} (position: {ann.get("position")}, style: {ann.get("style_hint")})')
            immersion = doc_exp.get('immersion_elements', [])
            if immersion:
                parts.append(f'IMMERSION ELEMENTS: {", ".join(immersion)}')
            exp_notes = '\n'.join(parts)

    # Build prompts
    # Determine tier
    tier = 1 if type_key in TIER1_TYPES else 2
    system_prompt = build_system_prompt(design_system, doc_type_info, tier=tier)
    user_prompt = build_user_prompt(doc, content, case_info, images, exp_notes)

    # Call Claude
    print(f'  Rendering {doc_id} ({type_key})...', end=' ', flush=True)
    try:
        html_raw, usage = call_litellm(model, system_prompt, user_prompt)
    except Exception as e:
        print(f'API ERROR: {e}')
        return False

    # Extract clean HTML
    html = extract_html(html_raw)

    # Write HTML
    layout_dir = case_dir / 'layout_specs'
    layout_dir.mkdir(parents=True, exist_ok=True)
    html_path = layout_dir / f'{doc_id}.html'
    html_path.write_text(html, encoding='utf-8')

    # Log cost
    cost = 0
    if tracker:
        cost = tracker.log_render_call(
            doc_id, model,
            usage.get('input_tokens', 0),
            usage.get('output_tokens', 0)
        )

    # Render PDF
    render_script = workspace / 'cases' / 'render' / 'render_pdf_system_chromium.js'
    pdf_path = layout_dir / f'{doc_id}.pdf'

    if render_script.exists():
        success, result = render_html_to_pdf(html_path, pdf_path, render_script)
        if success:
            print(f'OK ({result:.0f}KB) ${cost:.3f}')
        else:
            print(f'PDF FAIL: {result}')
            # Try alternative: direct chromium call
            try:
                chromium = '/usr/bin/chromium-browser'
                if not os.path.exists(chromium):
                    chromium = 'chromium-browser'
                subprocess.run([
                    chromium, '--headless=new', '--no-sandbox',
                    '--disable-gpu', '--print-to-pdf=' + str(pdf_path),
                    '--no-pdf-header-footer',
                    str(html_path.resolve())
                ], capture_output=True, timeout=30)
                if pdf_path.exists():
                    print(f'  (fallback OK: {pdf_path.stat().st_size / 1024:.0f}KB)')
                    return True
            except Exception:
                pass
            return False
    else:
        print(f'WARN: render script not found, HTML only')

    return True


def merge_envelope(case_dir, workspace, envelope, doc_ids):
    """Merge individual PDFs into envelope PDF."""
    merge_script = workspace / 'cases' / 'render' / 'merge_pdfs.js'
    final_dir = case_dir / 'final'
    final_dir.mkdir(parents=True, exist_ok=True)

    out_path = final_dir / f'Envelope_{envelope}.pdf'
    layout_dir = case_dir / 'layout_specs'

    pdf_paths = []
    for did in sorted(doc_ids):
        pdf = layout_dir / f'{did}.pdf'
        if pdf.exists() and pdf.stat().st_size > 500:
            pdf_paths.append(str(pdf))
        else:
            print(f'  WARN: {did}.pdf missing or empty')

    if not pdf_paths:
        print(f'  SKIP Envelope_{envelope}: no PDFs')
        return False

    try:
        args = ['node', str(merge_script), str(out_path)] + pdf_paths
        result = subprocess.run(args, capture_output=True, text=True, timeout=60)
        if result.returncode == 0 and out_path.exists():
            size_mb = out_path.stat().st_size / (1024 * 1024)
            print(f'  ✓ Envelope_{envelope}.pdf ({len(pdf_paths)} docs, {size_mb:.1f}MB)')
            return True
        else:
            print(f'  ✗ Envelope_{envelope}: {result.stderr[:200]}')
            return False
    except Exception as e:
        print(f'  ✗ Envelope_{envelope}: {e}')
        return False


def main():
    parser = argparse.ArgumentParser(description='AI Document Renderer V9')
    parser.add_argument('case_dir', help='Path to case export directory')
    parser.add_argument('--model', default=DEFAULT_MODEL,
                        help=f'LLM model for rendering (default: {DEFAULT_MODEL})')
    parser.add_argument('--skip-pdf', action='store_true',
                        help='Generate HTML only, skip PDF rendering')
    parser.add_argument('--doc', help='Render only this doc_id (e.g. A1)')
    parser.add_argument('--skip-merge', action='store_true',
                        help='Skip envelope merging')
    args = parser.parse_args()

    case_dir = Path(args.case_dir)
    workspace = find_workspace()

    if not workspace:
        print("ERROR: Cannot find workspace with cases/config/")
        sys.exit(1)

    config_dir = workspace / 'cases' / 'config'

    print(f'AI Document Renderer V9')
    print(f'Workspace: {workspace}')
    print(f'Case: {case_dir}')
    print(f'Model: {args.model}')
    print()

    # Load configs
    design_system = load_json(config_dir / 'design_system.json')
    type_catalog = load_json(config_dir / 'doc_type_catalog.json')
    catalog = load_json(case_dir / 'clue_catalog.json')
    plan = load_json(case_dir / 'case-plan.json')
    art_briefs = load_json(case_dir / 'art_briefs.json') if (case_dir / 'art_briefs.json').exists() else {'briefs': []}
    experience = load_json(case_dir / 'experience_design.json') if (case_dir / 'experience_design.json').exists() else {}

    # Build case info
    case_number = plan.get('case_number', plan.get('slug', 'CASE-001'))
    case_info = {
        'case_number': case_number,
        'title': plan.get('title', ''),
        'setting': plan.get('setting', {}),
    }

    # Initialize cost tracker
    sys.path.insert(0, str(workspace / 'cases' / 'scripts'))
    try:
        from cost_tracker import CostTracker
        tracker = CostTracker(case_dir)
    except ImportError:
        print("WARN: cost_tracker.py not found, costs will not be tracked")
        tracker = None

    # Collect documents
    documents = [d for d in catalog.get('documents', []) if isinstance(d, dict)]

    if args.doc:
        documents = [d for d in documents if d.get('doc_id') == args.doc]
        if not documents:
            print(f"ERROR: Document {args.doc} not found in clue_catalog")
            sys.exit(1)

    # Group by envelope
    envelopes = {}
    for doc in documents:
        env = doc.get('envelope', '?')
        if env not in envelopes:
            envelopes[env] = []
        envelopes[env].append(doc.get('doc_id', '?'))

    print(f'Documents: {len(documents)} total across {len(envelopes)} envelopes')
    print()

    # ── Render all documents ──
    print('═══ Rendering Documents ═══')
    success = 0
    errors = 0
    for doc in documents:
        ok = render_document(
            doc, case_dir, workspace, design_system, type_catalog,
            art_briefs, case_info, experience, args.model, tracker
        )
        if ok:
            success += 1
        else:
            errors += 1

    print()
    print(f'Rendered: {success} OK, {errors} errors')

    # ── Merge envelopes ──
    if not args.skip_merge and not args.skip_pdf:
        print()
        print('═══ Merging Envelopes ═══')
        for envelope, doc_ids in sorted(envelopes.items()):
            merge_envelope(case_dir, workspace, envelope, doc_ids)

    # ── Cost summary ──
    if tracker:
        print()
        print(tracker.get_summary())

    print()
    print('Done.')


if __name__ == '__main__':
    main()
