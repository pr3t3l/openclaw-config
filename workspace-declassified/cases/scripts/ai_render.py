def resize_image_to_base64(image_path, max_width=600):
    """Resize an image and return base64 data URI."""
    path = Path(image_path)
    if not path.exists():
        return None

    ext = path.suffix.lower().lstrip('.')
    if ext == 'jpg':
        ext = 'jpeg'
    mime = f'image/{ext}'

    if HAS_PIL:
        img = Image.open(path)
        if img.width > max_width:
            ratio = max_width / img.width
            new_size = (max_width, int(img.height * ratio))
            img = img.resize(new_size, Image.LANCZOS)
        buf = BytesIO()
        # Force JPEG output for smaller base64 size (TL-02: 15x cost reduction)
        if img.mode == 'RGBA':
            img = img.convert('RGB')
        img.save(buf, format='JPEG', quality=70)
        data = base64.b64encode(buf.getvalue()).decode()
        mime = 'image/jpeg'
    else:
        # No PIL — just encode at original size
        with open(path, 'rb') as f:
            data = base64.b64encode(f.read()).decode()

    return f'data:{mime};base64,{data}'


def call_litellm(model, system_prompt, user_prompt, max_tokens=MAX_TOKENS):
    """Call Anthropic API with streaming via curl (fixes WSL connection drops)."""
    api_key = os.environ.get('ANTHROPIC_API_KEY', '')
    if not api_key:
        raise ValueError('ANTHROPIC_API_KEY not set in environment')

    payload = json.dumps({
        'model': model,
        'max_tokens': max_tokens,
        'stream': True,
        'system': system_prompt,
        'messages': [{'role': 'user', 'content': user_prompt}]
    }, ensure_ascii=True)

    import tempfile
    with tempfile.NamedTemporaryFile(mode='wb', suffix='.json', delete=False) as f:
        f.write(payload.encode('utf-8'))
        tmp_path = f.name

    try:
        result = subprocess.run([
            'curl', '-s', '-S', '-N', '--max-time', '600',
            'https://api.anthropic.com/v1/messages',
            '-H', f'x-api-key: {api_key}',
            '-H', 'anthropic-version: 2023-06-01',
            '-H', 'content-type: application/json',
            '--data-binary', f'@{tmp_path}'
        ], capture_output=True, text=True, timeout=650)

        if result.returncode != 0:
            raise Exception(f'curl failed (rc={result.returncode}): {result.stderr[:200]}')

        if not result.stdout.strip():
            raise Exception(f'Empty response. stderr: {result.stderr[:200]}')

        # Parse SSE stream
        full_text = ''
        input_tokens = 0
        output_tokens = 0
        for line in result.stdout.split('\n'):
            if line.startswith('data: '):
                try:
                    event = json.loads(line[6:])
                    etype = event.get('type', '')
                    if etype == 'content_block_delta':
                        delta = event.get('delta', {})
                        if delta.get('type') == 'text_delta':
                            full_text += delta.get('text', '')
                    elif etype == 'message_delta':
                        usage = event.get('usage', {})
                        output_tokens = usage.get('output_tokens', output_tokens)
                    elif etype == 'message_start':
                        usage = event.get('message', {}).get('usage', {})
                        input_tokens = usage.get('input_tokens', 0)
                    elif etype == 'error':
                        err = event.get('error', {})
                        raise Exception(f'API error: {err.get("message", str(err))}')
                except json.JSONDecodeError:
                    pass

        if not full_text:
            raise Exception(f'No text in stream. Response starts: {result.stdout[:300]}')

        return full_text, {
            'input_tokens': input_tokens,
            'output_tokens': output_tokens,
            'total_tokens': input_tokens + output_tokens
        }
    finally:
        os.unlink(tmp_path)


def extract_html(text):
    """Extract HTML from Claude's response (may be wrapped in markdown fences)."""
    # Try to find HTML within code fences
    m = re.search(r'```html?\s*\n(.*?)```', text, re.DOTALL)
    if m:
        return m.group(1).strip()

    # Try to find <!DOCTYPE or <html or <style as start
    for marker in ['<!DOCTYPE', '<html', '<style', '<div']:
        idx = text.find(marker)
        if idx >= 0:
            return text[idx:].strip()

    # Return as-is (hopefully it's clean HTML)
    return text.strip()


def render_html_to_pdf(html_path, pdf_path, render_script):
    """Render HTML to PDF using Chromium."""
    try:
        result = subprocess.run(
            ['node', str(render_script), str(html_path), str(pdf_path)],
            capture_output=True, text=True, timeout=60
        )
        if result.returncode == 0 and Path(pdf_path).exists():
            size_kb = Path(pdf_path).stat().st_size / 1024
            return True, size_kb
        else:
            return False, result.stderr[:200]
    except Exception as e:
        return False, str(e)


